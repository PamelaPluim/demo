jQuery(document).ready(function(){
	jQuery('#email').keyup(function(){
		if(isValidEmailAddress(jQuery(this).val())){
			jQuery(this).css('border', '2px solid green');
		} else {
			jQuery(this).css('border', '2px solid red');
		}
	});
	jQuery('#password_confirm').keyup(function(){
		if(jQuery('#password').val() !== jQuery(this).val()){
			jQuery('#checkmark').hide();
			jQuery(this).css('border', '2px solid red');
			jQuery('#submitform').prop('disabled', true);
		} else {
			jQuery('#checkmark').show();
			jQuery(this).css('border', '2px solid green');
			jQuery('#submitform').prop('disabled', false);
		}
	});
});

